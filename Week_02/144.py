    def preorderTraversal(self, root: TreeNode) -> List[int]:
        if not root:
            return []
        stack, ans = [root], []
        while stack:
            p = stack.pop()
            if root is not None:
                ans.append(p.val)
                if p.right is not None:
                    stack.append(p.right)
                if p.left is not None:
                    stack.append(p.left)
        return ans
        
